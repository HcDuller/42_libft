char	*ft_strchr(const char *s, int c)
{
	char	*r;

	r = (char *)s;
	while (*r)
	{
		if (*r == c)
			return (r);
		r++;
		if (*r == c)
			return (r);
		if (!*r)
			return (0);
	}
	return (0);
}

char	*ft_strrchr(const char *s, int c)
{
	char	*r;
	int		i;

	r = (char *)s;
	while (*(r++))
		i++;
	while (i >= 0)
	{
		if (*r == c)
			return (r);
		r--;
		i--;
		if (i == 0 && !(*r == c))
			return (0);
	}
	return (0);
}
